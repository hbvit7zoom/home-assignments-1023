public class Main1 {

    public static int add(int i1, int i2) {
        return i1 + i2;
    }

    public static double add(double d1, double d2) {
        return d1 + d2;
    }

    public static void main(String[] args) {
        System.out.println(add(5, 5));
        System.out.println(add(0.05, 0.05));
    }
}
